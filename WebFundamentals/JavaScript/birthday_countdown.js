var daysUntilMyBirthday = 60;
for (var i = daysUntilMyBirthday; i >= 0; i--){
  if (i < 5){
    console.log("", i, "DAYS UNTIL MY BIRTHDAY!!! AHHHH!!!!! YAY!!!!!!");
  }else if(i < 30){
    console.log("It's", i, "days until my birthday! We are getting so close!");
  }else{
    console.log("", i, "days until my birthday....tooooooo loooooong =(");
  }
}
